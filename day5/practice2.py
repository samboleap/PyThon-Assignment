def total_aa(a,b,c):
    sum = a + b + c
    return sum

def total_bb(a,b,c,d):
    sum = a + b + c + d
    return sum
def total_cc(a,b,c,d,e):
    sum = a + b + c + d + e
    return sum

student_a = total_aa(40,90,48)
student_b = total_bb(60,45,32,54)
student_c = total_cc(89,65,75,24,76)

print(student_a)
print(student_b)
print(student_c)