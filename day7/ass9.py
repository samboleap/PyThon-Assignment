# Define a set of unique integers
my_set = {1, 2, 3, 4, 5}

# Check if an element is in the set using the "in" keyword
if 3 in my_set:
    print("3 is the set")
else:
    print("3 is not the set")

#Check if another element is in the set
if 6 in my_set:
    print("6 is in the set")
else:
    print("6 is not in the set")