def total_aa(a, b, c):
    sum = a + b + c
    return sum

def total_bb(a, b, c, d):
    sum = a + b + c + d
    return sum

def total_cc(a, b, c, d, e):
    sum = a + b + c + d + e 
    return sum

student_a = total_aa(60, 70, 65)
student_b = total_bb(50, 70, 46, 90)
student_c = total_cc(90, 40, 50, 70, 60)

print(student_a)
print(student_b)
print(student_c)